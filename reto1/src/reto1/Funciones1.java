package reto1;

public class Funciones1 {


	public static int suma(int n1, int n2) {
		return n1+n2;
	
	}
	
	public static int resta(int n1, int n2) {
		return n1-n2;
		}
	
		public static boolean esPar(int num) {
			if (num % 2 == 0) {
				return true;
			} else
				return false;
	}
		
		public static boolean esImpar(int num) {
			if (num % 2 != 0) {
				return true;
			} else
				return false;

}
}